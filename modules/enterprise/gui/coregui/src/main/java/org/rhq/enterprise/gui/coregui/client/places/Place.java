package org.rhq.enterprise.gui.coregui.client.places;

/**
 * @deprecated replaced by {@link org.rhq.enterprise.gui.coregui.client.View} and
 *             {@link org.rhq.enterprise.gui.coregui.client.Breadcrumb}
 *
 * @author Greg Hinkle
 */
@Deprecated
public class Place {

    private String id;
    private String name;

    public Place(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Place place = (Place) o;

        if (!id.equals(place.id)) return false;

        return true;
    }

    @Override
    public int hashCode() {
        return id.hashCode();
    }
}
